rm -rf $KERNEL_DIR/ 2>/dev/null || true
echo "Unpacking kernel sources"
tar -xJf $KERNEL_DIR.tar.xz
cp .config $KERNEL_DIR/

cd $KERNEL_DIR/

echo "Starting compilation test"
time make -j$(nproc)

exit 0
